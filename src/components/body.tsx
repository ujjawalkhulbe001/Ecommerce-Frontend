import React, { useContext, useEffect, useState } from "react";
import axios from "axios";
import router from "next/router";
import AppContext from "../../context/AppContext";
interface Product {
  sku_id: string;
  name: string;
  description: string;
  price: number;
  images: { url: string }[];
}

interface ProductCardProps {
  product: Product;
}

const ProductCard: React.FC<ProductCardProps> = ({ product }) => {
  // useEffect(() => {
  //     console.log(product)
  // }, [])

  if (!product) {
    return <div>product</div>;
  }

  const handleNavigation = (id: any) => {
    router.push(`/Detailpage?productId=${id}`);
  };

  return (
    <div
      className="max-w-xs rounded overflow-hidden shadow-lg m-4"
      onClick={() => handleNavigation(product._id)}
    >
      <img className="w-auto" src={product?.images[0].url} alt={product.name} />
      <div className="px-0 py-0">
        <div className="font-bold text-xl mb-2">{product.name}</div>
        {/* <p className="text-gray-700 text-base">{product.description}</p> */}
        <p className="mt-2 text-gray-600">${product.price}</p>
      </div>
    </div>
  );
};

const ProductListing: React.FC = () => {
  const [products, setProducts] = useState<Product[]>([]);
  const { search, searchProducts } = useContext(AppContext) as any;
  const [pages, setPages] = useState(0);
  const [currPage, setCurrPage] = useState(1);

  useEffect(() => {
    fetchProducts();
  }, [currPage]);

  const fetchProducts = async () => {
    try {
      // console.log(localStorage.getItem("token"))
      const response = await axios.get(
        `http://localhost:9000/Product/Products?page=${currPage}`,
        {
          headers: { authorization: localStorage.getItem("token") },
        },
      );
      console.log(response.data.data.totalPages);
      setPages(response.data.data.totalPages);
      setProducts(response.data.data.products);
    } catch (error) {
      console.error("Error fetching product data:", error);
    }
  };

  return (
    <div className="container mx-auto">
      <div className="grid grid-cols-4 gap-4 justify-center">
        {products.map((product, idx) => {
          return (
            <ProductCard product={product} key={product._id}></ProductCard>
          );
        })}
      </div>

      <div className="flex justify-center mt-4 gap-6 border-black">
        <button
          className="mx-1 px-3 py-1 border rounded"
          onClick={() => setCurrPage(currPage - 1)}
          disabled={currPage === 1}
        >
          Previous
        </button>
        {Array.from({ length: pages }, (_, i) => {
          return (
            <button
              key={i}
              onClick={() => setCurrPage(i + 1)}
              className={`mx-1 px-3 py-1 border rounded ${
                currPage === i + 1 ? "bg-blue-500 text-white" : ""
              }`}
            >
              {i + 1}
            </button>
          );
        })}
        <button
          className="mx-1 px-3 py-1 border rounded"
          onClick={() => setCurrPage(currPage + 1)}
          disabled={currPage >= pages}
        >
          Next
        </button>
      </div>

      <div className="flex justify-center mt-4 gap-6 border-black">
        {/* <h1 className="text-3xl">{pages}</h1> */}
        {/* Pagination buttons */}
        {/* <button
          className="mx-1 px-3 py-1 border rounded"
          onClick={() => setCurrentPage(currentPage - 1)}
          disabled={currentPage === 1}
        >
          Previous
        </button>
        <span className="mx-1">{currentPage}</span>
        <button
          className="mx-1 px-3 py-1 border rounded"
          onClick={() => setCurrentPage(currentPage + 1)}
          disabled={indexOfLastProduct >= products.length}
        >
          Next
        </button> */}

        {Array.from({ length: pages }, (_, i) => {
          return i + 1;
        }).map((val, idx) => {
          return <button onClick={() => setCurrPage(val)}>{val}</button>;
        })}
        {search
          ? products.map((product) => (
              <ProductCard key={product._id} product={product} />
            ))
          : searchProducts?.map((product) => (
              <ProductCard key={product._id} product={product} />
            ))}
      </div>
    </div>
  );
};

export default ProductListing;

